package jan;

public class AllArithmeticalOperations {
	public static void main(String args[]) {
		int a,b;
		a=6;
		b=3;
		int sum=a+b;
		System.out.println("Add two numbers: "+sum);
		int sub=a-b;
		System.out.println("Subtract two numbers: "+sub);
		int multi=a*b;
		System.out.println("product of two numbers: "+multi);
		int div=a/b;
		System.out.println("division of two numbers: "+div);
		int mod=a%b;
		System.out.println("modulo of two numbers: "+mod);
		
	}

}
